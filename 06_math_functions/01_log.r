# --- 1. Funciones Logarítmicas y Exponenciales ---
# Descripción:
#   `log()`: Logaritmo natural (base e).
#   `log10()`: Logaritmo en base 10.
#   `log2()`: Logaritmo en base 2.
#   `exp()`: Exponencial (e^x).

cat("--- 1. Demostración de Funciones Logarítmicas y Exponenciales ---\n")
x <- 10
print(paste("log(x):", log(x)))
print(paste("log10(x):", log10(x)))
print(paste("log2(x):", log2(x)))
print(paste("exp(x):", exp(x)))
cat("\n")