# --- 1. Operadores Aritméticos ---
# Descripción:
#   Realizan operaciones matemáticas básicas.
# Operadores: +, -, *, /, ^ (potencia), %% (módulo), %/% (división entera)

cat("--- 1. Demostración de Operadores Aritméticos ---\n")
a <- 10
b <- 3

print(paste("Suma (a + b):", a + b))
print(paste("Resta (a - b):", a - b))
print(paste("Multiplicación (a * b):", a * b))
print(paste("División (a / b):", a / b))
print(paste("Potencia (a ^ b):", a ^ b))
print(paste("Módulo (a %% b):", a %% b, "(resto de la división)"))
print(paste("División Entera (a %/% b):", a %/% b, "(cociente entero)"))
cat("\n")