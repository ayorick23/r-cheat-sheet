# --- 4. Operadores de Asignación ---
# Descripción:
#   Se utilizan para asignar valores a variables.
# Operadores: <- (asignación preferida), = (también asigna), -> (asignación inversa)

cat("--- 4. Demostración de Operadores de Asignación ---\n")
mi_variable_1 <- 100
print(paste("Asignación con <- :", mi_variable_1))

mi_variable_2 = 200
print(paste("Asignación con = :", mi_variable_2))

300 -> mi_variable_3
print(paste("Asignación inversa con -> :", mi_variable_3))
cat("\n")