# --- 2. Operadores Relacionales ---
# Descripción:
#   Comparan dos valores y devuelven un resultado booleano (TRUE o FALSE).
# Operadores: == (igual a), != (diferente de), < (menor que), > (mayor que),
#             <= (menor o igual que), >= (mayor o igual que)

cat("--- 2. Demostración de Operadores Relacionales ---\n")
x <- 7
y <- 12
z <- 7

print(paste("x == y:", x == y))
print(paste("x != y:", x != y))
print(paste("x < y:", x < y))
print(paste("x > y:", x > y))
print(paste("x <= z:", x <= z))
print(paste("y >= x:", y >= x))
cat("\n")